extern volatile uint32_t TimeSecs;
extern volatile uint16_t SW3;
extern volatile uint16_t SW2;
extern volatile uint16_t SW1;
extern uint16_t Menu;
extern volatile uint16_t AlarmOn;
extern volatile uint16_t TimeOut;

uint16_t hrs2 = 0;
uint16_t mins2 = 0;
uint16_t meridiem2 = 0;

uint16_t hours2 = 0;
volatile uint32_t AlarmTime = 0;

void Draw_Alarmset(void){
  hours2 = 0;
  uint32_t time = TimeSecs;
  hrs2 = time/3600;
  mins2 = (time%3600)/60;
  meridiem2 = 0;
  if(hrs2 >= 12){
    meridiem2 = 15;
  }
  Output_Clear();
  ST7735_DrawBitmap(65, 95, arrowdown2, 30, 20);
  ST7735_DrawBitmap(65, 35, arrowup2, 30, 20);
  TimeOut = 61;
  Refresh_Alarmset();
}

void Refresh_Alarmset(void){
  if(SW3 | SW2 | SW1){
    if(SW3){          // enter
      if(hours2){
        uint32_t time = 0;
        time += hrs2*60*60;
        time += mins2*60;
        TimeOut = 0;
        Menu = 1;
        SW3 = SW2 = SW1 = 0;
        Output_Clear();
        AlarmOn = 1;
        Draw_Menu();
        AlarmTime = time;
        return;
      }
      Output_Clear();
      ST7735_DrawBitmap(10, 95, arrowdown2, 30, 20);
      ST7735_DrawBitmap(10, 35, arrowup2, 30, 20);
      hours2 = 1;
    }
    else if(SW2){     // up
      if(hours2){
        hrs2 = (hrs2 + 1)%24;
        if(hrs2 >= 12){
          meridiem2 = 15;
        }
        else{
          meridiem2 = 0;
        }
      }
      else{
        mins2 = (mins2 + 1)%60;
      }
    }
    else if(SW1){     // down
      if(hours2){
        if(!hrs2){
          hrs2 = 23;
        }
        else{
          hrs2--;
        }
        if(hrs2 >= 12){
          meridiem2 = 15;
        }
        else{
          meridiem2 = 0;
        }
      }
      else{
        if(!mins2){
          mins2 = 59;
        }
        else{
          mins2--;
        }
      }
    }
    SW3 = SW2 = SW1 = 0;
  }
  
  uint16_t hrs_dis = hrs2;
  if(hrs_dis >= 12){
    hrs_dis -= 12;
  }
  if(!hrs_dis){
    hrs_dis = 12;
  }
  uint16_t x = hrs_dis/10;
  if(x){
    ST7735_DrawChar(0, 0 + YPOS, x + '0', ST7735_YELLOW, ST7735_BLACK, 4);
  }
  else{
    ST7735_DrawChar(0, 0 + YPOS, ' ', ST7735_YELLOW, ST7735_BLACK, 4);
  }
  ST7735_DrawChar(25, 0 + YPOS, hrs_dis%10 + '0', ST7735_YELLOW, ST7735_BLACK, 4);
  ST7735_FillRect(50, 7 + YPOS, 5, 5, ST7735_YELLOW);
  ST7735_FillRect(50, 17 + YPOS, 5, 5, ST7735_YELLOW);
  ST7735_DrawChar(60, 0 + YPOS, mins2/10 + '0', ST7735_YELLOW, ST7735_BLACK, 4);
  ST7735_DrawChar(85, 0 + YPOS, mins2%10 + '0', ST7735_YELLOW, ST7735_BLACK, 4);
  ST7735_DrawChar(113, 20 + YPOS, 'a' + meridiem2, ST7735_YELLOW, ST7735_BLACK, 1);
  ST7735_DrawChar(120, 20 + YPOS, 'm', ST7735_YELLOW, ST7735_BLACK, 1);
}

